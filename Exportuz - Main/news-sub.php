                                                <p>Ўзбекистонда биринчи ярим йилликда саноат маҳсулоти ҳажми 167 триллион сўмни ташкил этиб, ўтган йилгига нисбатан 1,9 фоизга камайган.
Ўзбекистон Республикаси Президенти Шавкат Мирзиёев раислигида 12 август куни ҳудудларда саноатни ривожлантириш, рақобатбардош маҳсулотлар ишлаб чиқаришни кенгайтириш масалалари бўйича видеоселектор йиғилиши ўтказилди.</p>
<p>Саноат корхоналари иқтисодий барқарорликни таъминлаш, иш ўринларини сақлаб қолиш ва янгиларини яратишда муҳим ўрин тутади. Шу боис пандемияга қарамасдан мамлакатимизда бундай корхоналарнинг узлуксиз ишлаши учун шароитлар яратиб берилди. Бунинг натижасида йилнинг биринчи ярмида “Ўзэлтехсаноат”, “Ўзавтосаноат”, тўқимачилик, чарм-пойабзал, металлургия, электр энергияси тармоқларида ўсишга эришилган. Қўрғонтепа, Қумқўрғон, Ромитан, Музработ, Избоскан, Норин ва Янгибозор туманларида саноат ишлаб чиқариш ҳажми бир неча баробар кўпайган.</p>
<div class="general-text">
