                                    <li>Мисол учун, Қорақалпоғистонда иккинчи ярим йилликда 7,8 триллион сўмлик саноат маҳсулоти ишлаб чиқариш режалаштирилган. 5 та йирик лойиҳани фойдаланишга топшириш мўлжалланган. Тўқимачилик, кимё тармоқларидаги бу лойиҳалар ҳисобидан 500 та янги иш ўрни ташкил этилади.</li>
